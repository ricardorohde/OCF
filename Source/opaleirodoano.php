<p class=MsoNormal><span style='font-size:10.0pt'><span style='mso-tab-count:
1'>                </span><b style='mso-bidi-font-weight:normal'><u>Regulamento<o:p></o:p></u></b></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>O vencedor será aquele que tiver o maior número de
pontos até o final do período que se encerra no dia 20/11.<o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:10.0pt'><span style='mso-tab-count:
2'>                               </span>Somente os associados podem participar
do programa de fidelidade.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>O programa terá início no dia 28/04/2008 já computando
as mensalidades do mês 04 para a pontuação e considerando também as mensagens
no mural e fórum a partir dessa data.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>A participação nas reuniões e eventos será registrada
através de lista de presença preenchida pela diretoria ou outro sócio
designado.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>A pontuação por trazer novos sócios só será efetivada
após o pagamento da taxa de inscrição e primeira parcela do novo sócio.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>Para os novos associados será feita <span class=GramE>a</span>
média de pontos do primeiro colocado com o último na data de inclusão do novo
sócio.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>O primeiro critério de desempate será a participação
nas reuniões, o segundo a participação nos eventos, o terceiro o pagamento de
mensalidades e em quarto as mensagens no mural ou fórum do <span class=SpellE>site</span>.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:35.4pt;text-indent:35.4pt'><span
style='font-size:10.0pt'>O ranking de classificação assim como o extrato de
pontos estará disponível no <span class=SpellE>site</span> ou impresso caso
solicitado pelo associado.<o:p></o:p></span></p>

