ganhou Em caso de vitória do visitante soma-se 2 pontos ao 8,5 ou 3 Em caso de
empate: 9 pontos acertar em cheio 6 pontos errando o placar.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>6-Serão aceitos palpites até 15 minutos antes de começar a
RODADA e não de cada jogo como foi no Paulista em virtude da realização
simultânea da Copa Bolão.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>7-Haverá Bônus de 12 pontos para quem fizer mais pontos em
uma única <span class=GramE>rodada.</span>Se houver empate será dado o Bônus
para quem fizer esta pontuação mais precocemente no Bolão.Se o empate ocorrer
na mesma rodada os participantes empatados levarão todos eles os 12 pontos de Bônus.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>8-Haverá Bônus de 12 pontos para quem ganhar <span
class=GramE>2</span> rodadas. <span class=GramE>2</span> quintos lugares valem
1 quarto;2 quartos lugares valem 1 terceiro e assim por diante.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>9-Ganhará o Bolão quem fizer mais pontos no total Se houver
empate entre <span class=GramE>2</span> ou mais participantes será vencedor o
que tiver mais pontos descartando-se os Bônus.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

