<p class=MsoTitle>BOLÃO BRASILEIRÃO 2005 – RETURNO</p>

<p class=MsoNormal align=center style='text-align:center'><b><span
style='font-size:18.0pt;mso-bidi-font-size:12.0pt'>REGULAMENTO<o:p></o:p></span></b></p>

<p class=MsoNormal align=center style='text-align:center'><b><span
style='font-size:18.0pt;mso-bidi-font-size:12.0pt'><o:p>&nbsp;</o:p></span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:18.0pt;
mso-bidi-font-size:12.0pt'><o:p>&nbsp;</o:p></span></b></p>

<p class=MsoBodyText>Para participar do Bolão, envie seus palpites para a
primeira rodada do returno <span style='mso-spacerun:yes'> </span>do Campeonato
Brasileiro de Futebol da série A de 2005 até às 19h15min do dia 24/08/05 – Quarta-feira
(os jogos que abrem a rodada serão às 19h30min deste<span
style='mso-spacerun:yes'>  </span>dia) para o site <a
href="http://bolao.sytes.net/">http://bolao.sytes.net</a> . </p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'><o:p>&nbsp;</o:p></b></p>

<p class=MsoNormal style='text-align:justify'><span class=GramE><b
style='mso-bidi-font-weight:normal'>PONTUAÇÃO </b></span><b style='mso-bidi-font-weight:
normal'><o:p></o:p></b></p>

<p class=MsoNormal style='text-align:justify'>Serão contados pontos para todos
os palpites nas 21 rodadas de 11 jogos, fazendo-se um ranking geral de
pontuação considerando-se todas as 231 partidas do segundo turno do Campeonato
Brasileiro Série A, seguindo o seguinte critério:</p>

<p class=MsoNormal style='text-align:justify'>Nos casos de jogos com vitória de
um dos times:</p>

<p class=MsoNormal style='text-align:justify'><span class=GramE>8</span> pontos
– acerto em cheio (placar exato).</p>

<p class=MsoNormal style='text-align:justify'><span class=GramE>5</span> pontos
– acerto do vencedor<span style='mso-spacerun:yes'>  </span>pelo saldo de gols
correto, errando o placar exato.</p>

<p class=MsoNormal style='text-align:justify'><span class=GramE>3</span> pontos
– acerto do vencedor, errando o placar e o saldo de gols da vitória. </p>

<p class=MsoNormal style='text-align:justify'>Nos casos de <span class=GramE>empate:
</span></p>

<p class=MsoNormal style='text-align:justify'><span class=GramE>9</span> pontos
– acerto em cheio.</p>

<p class=MsoNormal style='text-align:justify'><span class=GramE>6</span> pontos
– acerto do empate com número de gols diferente do palpite.</p>

<p class=MsoNormal style='text-align:justify'>Obs - Haverá um bônus de <span
class=GramE>2</span> pontos nas três categorias de acerto nos casos de vitória
do time visitante da partida (nome colocado na segunda coluna do jogo, mesmo
nos casos de clássicos), valendo portanto 10, 7 e 5 pontos respectivamente. </p>

<p class=MsoNormal style='text-align:justify'>BÔNUS: Em todas as rodadas
completas de 11 jogos, serão observados os <span class=GramE>5</span> primeiros
colocados<span style='mso-spacerun:yes'>  </span>(sem critérios de desempate)
da rodada. Cada vez que um participante conseguir <span class=GramE>2</span>
títulos de rodada, será bonificado com 12 pontos. Cada <span class=GramE>2</span>
quintos lugares valem um quarto lugar, dois quartos valem um terceiro, dois
terceiros valem um segundo e 2 vice campeonatos de rodada valem um título. Ao
final do campeonato, o participante que tiver feito o maior número de pontos em
uma única rodada (RECORDE) também receberá um BÔNUS de 12 pontos, além de um
prêmio extra de 5% do rateio do bolão. </p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'><u><o:p><span style='text-decoration:none'>&nbsp;</span></o:p></u></b></p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'><u>CRITÉRIOS DE <span class=GramE>DESEMPATE:<span style='font-weight:
normal;text-decoration:none;text-underline:none'> </span></span></u></b></p>

<p class=MsoNormal style='text-align:justify'>PARA O DESEMPATE NA CLASSIFICAÇÃO
FINAL DO BOLÃO, VALE QUEM FEZ MAIS PONTOS NOS RESULTADOS DOS JOGOS,
EXCLUINDO-SE OS BÔNUS. SEGUNDO CRITÉRIO É O DESEMPENHO DO PARTICIPANTE NA
ÚLTIMA RODADA DO CAMPEONATO (GANHA O QUE TIVER MAIS PONTOS NELA), SEGUIDO PELA
PENÚLTIMA E ASSIM SUCESSIVAMENTE. </p>

<p class=MsoNormal style='text-align:justify'>PARA O DESEMPATE ENTRE OS
RECORDISTAS DE RODADAS, CONSIDERAREMOS QUEM TIVER ATINGIDO A MARCA MAIS
PRECOCEMENTE NO CAMPEONATO. SE OS PARTICIPANTES EMPATADOS TIVEREM FEITO A MARCA
NA MESMA RODADA, VALE A SEGUNDA MAIOR PONTUAÇÃO DE CADA UM E ASSIM POR DIANTE. </p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'><span style='font-size:14.0pt'>ATENÇÃO<span class=GramE>!!!!!!</span></span></b>
<b style='mso-bidi-font-weight:normal'>O participante que não enviar os palpites
dos 11 jogos para uma determinada rodada até 15 min<span class=GramE> <span
style='mso-spacerun:yes'> </span></span>antes de seu início, ficará sem palpites
naquela rodada. Estaremos deixando todas as rodadas disponíveis no site até o
dia 07/09/05 (terceira rodada). Para não <span class=GramE>correr riscos,
sugerimos</span> aos “esquecidos” ou “atarefados” que cadastrem um palpite
padrão até o final do campeonato, sendo aceitas alterações nos palpites até o
prazo limite (já citado) para cada rodada. Mandem um palpite padrão (tipo tudo
1x0 ou tudo 2x1) no início do campeonato e vão mudando conforme quiserem rodada
<span class=GramE>após</span> rodada para não correr o risco de zerar por falta
de palpite. NO CASO DE FALHAS NO SERVIDOR DO SITE DENTRO DO PRAZO LIMITE QUE
IMPOSSIBILITEM O ENVIO DE PALPITE PARA UMA RODADA, SERÃO ACEITOS PALPITES (OU
ALTERAÇÕES) ENVIADOS PARA O E-MAIL <a href="mailto:bolaodoalex@yahoo.com.br">bolaodoalex@yahoo.com.br</a>
DENTRO DO REFERIDO PRAZO. È NECESSÁRIO SALVAR os e-mails enviados na sua pasta
de mensagens enviadas dentro do próprio e-mail (o servidor de e-mails
geralmente faz isso automaticamente se programado), sendo este e-mail arquivado
a única prova aceita de palpite feito para <span class=GramE>rodada em caso de
não recebimento</span> por qualquer motivo. <o:p></o:p></b></p>

