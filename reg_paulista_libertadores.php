<p class=MsoNormal><span class=GramE>Regulamento Bolão Paulista-Libertadores
2006</span></p>

<p class=MsoNormal>1-O bolão se inicia dia 25/01/2006-Início da participação
brasileira <span class=GramE>na libertadores</span> e termina dia 20/04/2006
dia de término da 1ª fase da libertadores.Estão incluídos no bolão todos os
jogos do Paulista à partir de 25/01/2006 e os jogos dos times brasileiros na
repescagem e fase de grupos da libertadores.</p>

<p class=MsoNormal>2-A taxa de inscrição será 10,00 <span class=GramE>reais(</span>
exceto para Alex e Cacá co-organizadores e Alencar mantenedor do site)Não
haverá taxa de administração.</p>

<p class=MsoNormal>3-Pontuação: </p>

<p class=MsoNormal>Acertar em cheio – 8 pontos</p>

<p class=MsoNormal>Acertar a diferença- 5 pontos</p>

<p class=MsoNormal>Acertar empate errando resultado- 6 pontos</p>

<p class=MsoNormal>Acertar empate em cheio-9 pontos</p>

<p class=MsoNormal>Para quem acertar o <span class=GramE>resultado(</span>vencedor)
colocando vitória do visitante ,há acréscimo de 2 pontos na pontuação</p>

<p class=MsoNormal>Nos jogos <span class=GramE>da libertadores</span> vale a
pontuação acima ,com acréscimo de 4 pontos.(jogos de ouro)</p>

<p class=MsoNormal>4-Não haverá bônus.</p>

<p class=MsoNormal>5-Quem somar mais pontos ao final de todos os jogos será o
Campeão do Bolão</p>

<p class=MsoNormal>6-Se houver empate entre <span class=GramE>2</span>
participantes ,será vencedor o que fizer mais pontos em jogos da
libertadores.Este é o único critério de desempate.</p>

<p class=MsoNormal>7-Os palpites serão aceitos até 15 minutos antes do início
de cada <span class=GramE>jogo.</span>Se um participante esqueceu de palpitar
para sábado ,por exemplo,ele poderá palpitar para domingo,não perdendo
,assim,toda a rodada.</p>

<p class=MsoNormal>8-Premiação:</p>

<p class=MsoNormal>70% do bolo para o Campeão</p>

<p class=MsoNormal>20% para o vice</p>

<p class=MsoNormal>10% para o 3º colocado.</p>

<p class=MsoNormal>Boa sorte.</p>

